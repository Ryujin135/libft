#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	size;
	char	*str;
	int		i;

	i = 0;
	size = ft_strlen(s1) + ft_strlen(s2);
	if ((str = malloc(sizeof(char) * size + 1)) == NULL)
		return (NULL);
	while (*s1)
		str[i++] = *s1++;
	while (*s2)
		str[i++] = *s2++;
	str[i] = '\0';
	return (str);
}
